    public void Load()
    {
        RespondImpl advertisement = new RespondImpl();
        List<Respond> list = advertisement.list();
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for(Respond res: list)
        {
            int sid = res.getId();
            int user_id=res.getUser_id();
           // String userid = res.getUserid();
            int complaint_id = res.getComplaint_id();       
            String date = res.getResponse_date();
            String body = res.getResponse_body();    
            DFT.addRow(new Object[]{sid,user_id, complaint_id,date,body});
        }    
  
    }
    
    
    public void LoadUserID(){
        try {
            Connection con = FarmingConnection.getConnection();
            String sql = "SELECT id FROM users";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            jcuser_id.removeAllItems();
            while(rs.next()){
                jcuser_id.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error");
        } 
    }
    
    
    public void  LoadUserCompaint(){ //to load usercomplaint
       try {Connection con = FarmingConnection.getConnection();
            String sql = "SELECT user_id,message FROM complain";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            jcComplaindId.removeAllItems();
            compaintIDMessage.removeAllItems();
  
            while(rs.next()){
            jcComplaindId.addItem(rs.getString(1));  
            compaintIDMessage.addItem(rs.getString(2));}
          
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error");
        }   
    }
    

    
    
    private void btnPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPostActionPerformed
        Respond res = new Respond();
      
        String resbody = response_body.getText();
        String responddate = response_date.getText(); 
        int userid = Integer.parseInt( String.valueOf(jcuser_id.getSelectedItem()));
         useridcom = Integer.parseInt( String.valueOf(jcComplaindId.getSelectedItem()));
      
        res.setUser_id(userid);
        res.setComplaint_id(useridcom);
        res.setResponse_date(responddate);
        res.setResponse_body(resbody);
        
        RespondImpl advertisement = new RespondImpl();
        advertisement.postResponse(res);
        Load();
        response_body.setText("");

    }//GEN-LAST:event_btnPostActionPerformed
      
