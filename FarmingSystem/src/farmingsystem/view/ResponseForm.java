    private void btnSearchUserIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchUserIDActionPerformed
        search = Integer.parseInt(JOptionPane.showInputDialog("Enter User ID"));

        RespondImpl respond = new RespondImpl();

        List<Respond> list = respond.list();
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for(Respond res: list)
        {
           int sid = res.getId();
            int user_id=res.getUser_id();
           
            int complaint_id = res.getComplaint_id();       
            String date = res.getResponse_date();
            String body = res.getResponse_body();      
            if(search==user_id){
                DFT.addRow(new Object[]{sid,user_id, complaint_id,date,body});
            }else{
                System.out.println("error");
            }
        }             
    }//GEN-LAST:event_btnSearchUserIDActionPerformed

