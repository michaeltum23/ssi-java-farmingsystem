    public void Load()
    {
        TrainingImpl training = new TrainingImpl();
        List<Training> list = training.list();
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for(Training tr: list)
        {
            int id = tr.getId();
            String title = tr.getTitle();
            String desc = tr.getDescription();
            String schedDate = tr.getScheduleDate();
            String schedTime = tr.getSchedulteTime();
            String status =tr.getStatus();
  
            DFT.addRow(new Object[]{id,title,desc,schedDate,schedTime,status});
        }    
  
    }
        int search;
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        search = Integer.parseInt(JOptionPane.showInputDialog("Enter Crop ID"));
     
        TrainingImpl tr = new TrainingImpl();
        Training training  = tr.get(search);
        
        
        text_title.setText(training.getTitle());
        text_desc.setText(training.getDescription());
        text_schedDate.setText(training.getScheduleDate());
        text_schedTime.setText(training.getSchedulteTime());
        cmb_status.setSelectedItem(training.getStatus());

    }//GEN-LAST:event_btnSearchActionPerformed

    private void uploadImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadImageActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter fileFilter = new FileNameExtensionFilter("Image Type", "png", "jpg", "jpeg");
        fileChooser.addChoosableFileFilter(fileFilter);
        int load = fileChooser.showOpenDialog(null);
        if (load == fileChooser.APPROVE_OPTION) {

            File f = fileChooser.getSelectedFile();
            String path = f.getAbsolutePath();
            label_path.setText(path);
            System.out.println("Path: " + path);
            ImageIcon ii = new ImageIcon(path);
            Image img = ii.getImage().getScaledInstance(110, 110, Image.SCALE_SMOOTH);
            trainingImage.setText("");
            trainingImage.setIcon(new ImageIcon(img));
            
        }
    }//GEN-LAST:event_uploadImageActionPerformed
        File f;
        InputStream is;
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Training training = new Training();
        f = new File(label_path.getText());
        System.out.println(f);
        try {
            is = new FileInputStream(f);
        } catch (FileNotFoundException ex) {
        }
        training.setTrainingImage(is);
        training.setFile(f);
        
        String trainingTitle = text_title.getText();
        String trainingDesc = text_desc.getText();
        String scheduleDate = text_schedDate.getText();
        String scheduleTime = text_schedTime.getText();
        String status = cmb_status.getSelectedItem().toString();
        
        training.setTitle(trainingTitle);
        training.setDescription(trainingDesc);
        training.setScheduleDate(scheduleDate);
        training.setSchedulteTime(scheduleTime);
        training.setStatus(status);
        
        TrainingImpl tr = new TrainingImpl();
        tr.addTraining(training);
        Load();
        text_title.setText("");
        text_desc.setText("");
        text_schedDate.setText("");
        text_schedTime.setText("");
        text_title.requestFocus();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Training training = new Training();
 
        String trainingTitle = text_title.getText();
        String trainingDesc = text_desc.getText();
        String scheduleDate = text_schedDate.getText();
        String scheduleTime = text_schedTime.getText();
        String status = cmb_status.getSelectedItem().toString();
        
        training.setTitle(trainingTitle);
        training.setDescription(trainingDesc);
        training.setScheduleDate(scheduleDate);
        training.setSchedulteTime(scheduleTime);
        training.setStatus(status);
        
        training.setId(search);
        
        TrainingImpl tr = new TrainingImpl();
        tr.updateTraining(training);
        Load();
        text_title.setText("");
        text_desc.setText("");
        text_schedDate.setText("");
        text_schedTime.setText("");
        text_title.requestFocus();

    }//GEN-LAST:event_btnUpdateActionPerformed

