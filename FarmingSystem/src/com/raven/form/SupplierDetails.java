    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        User user = new User();
        
        String firstName = textFirstName.getText();
        String middleName = textMiddleName.getText();
        String lastName = textLastName.getText();
        String email=textEmail.getText();
        String bdate = textBDate.getText();
        String civilStats = cmbCStatus.getSelectedItem().toString();
        rbnMale.setActionCommand("Male");
        rbnFemale.setActionCommand("Female");
        String gender = buttonGender.getSelection().getActionCommand();
        String contact_no = textContactNo.getText();
        String house_no =textHouseNo.getText();
        String street = textStreet.getText();
        String city = cmbCity.getSelectedItem().toString();
        String province= cmbProvince.getSelectedItem().toString();
        int zipCode = Integer.parseInt(textPostalcode.getText());
        
             
        user.setFirstName(firstName);
        user.setMiddleName(middleName);
        user.setLastName(lastName);
        user.setEmail(email);
        user.setBirthDate(bdate);
        user.setCivilStatus(civilStats);
        user.setGender(gender);
        user.setContactNumber(contact_no);
        user.setHouseNo(house_no);
        user.setStreetAddress(street);
        user.setCityAddress(city);
        user.setProvince(province);
        user.setZipCode(zipCode);
        
        UserImp ui = new UserImp();
        ui.update(user);
        this.dispose();
    }//GEN-LAST:event_updateButtonActionPerformed

