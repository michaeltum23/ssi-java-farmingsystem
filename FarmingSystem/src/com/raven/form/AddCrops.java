    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        Crops crop = new Crops();
        
        
        int id = Integer.parseInt(jLabel1.getText());
        String cropName = textCropName.getText();
        double price = Double.parseDouble(textPrice.getText());
        double quantity = Double.parseDouble(textQuantity.getText());
        
        f = new File(lblFilePath.getText());
        System.out.println(f);
        try {
            is = new FileInputStream(f);
        } catch (FileNotFoundException ex) {
        }
        crop.setCropImage(is);
        crop.setFile(f);

        crop.setUserID(id);
        crop.setCropName(cropName);
        crop.setPrice(price);
        crop.setQuantity(quantity);

        CropsImpl cs = new CropsImpl();
        cs.addCrops(crop);
        this.dispose();
    }//GEN-LAST:event_updateButtonActionPerformed

